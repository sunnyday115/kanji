const main = () => {
    const jsonPath = path.join(process.cwd(), 'src/data/kanji.json');
    const data = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));

    // データを更新
    let updatedCount = 0;
    const updatedData = data.map(item => {
        if (richData[item.kanji]) {
            updatedCount++;
            return {
                ...item,
                ...richData[item.kanji]
            };
        }
        return item;
    });

    fs.writeFileSync(jsonPath, JSON.stringify(updatedData, null, 2));
    console.log(`Successfully updated ${updatedCount} kanji with rich data.`);
};

main();
